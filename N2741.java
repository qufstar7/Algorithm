import java.io.*;
public class N2741 {
	public static void main(String[] args) throws NumberFormatException, IOException {
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		int N = Integer.parseInt(br.readLine());
		for (int i = 1; i < N+1; i++) {
			System.out.println(i);
		}		
	}
}